# Make a Program that asks for the 4 bimonthly grades and shows the average.

grade1 = float(input("Enter grade1: "))
grade2 = float(input("Enter grade2: "))
grade3 = float(input("Enter grade3: "))
grade4 = float(input("Enter grade4: "))

average = (grade1 + grade2 + grade3 + grade4) / 4

print("The average of the 4 grades entered is: ", average)
